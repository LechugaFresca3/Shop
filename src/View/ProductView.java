    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        // TODO add your handling code here:
        switch (option) {
            case 2:

                //System.out.println("en el 2");
                //  leer datos de la ventana
                String name = NProduct.getText();
                int stock = Integer.parseInt(SProduct.getText());
                double price = Double.parseDouble(PProduct.getText());
                Amount wholesalerPrice = new Amount(price);

                if (shop.findProduct(name) == null) {

                    Product p = new Product(name, wholesalerPrice, true, stock);
                    shop.addProduct(p);
                    JOptionPane.showMessageDialog(this, name + " ańadido correctamente", "Info", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, " Este producto ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                }

                break;
            case 3:
                //System.out.println("en el 3");

                // add stock product
                String nameStock = NProduct.getText();
                Product found = shop.findProduct(nameStock);

                if (found != null) {
                    int stock3 = Integer.parseInt(SProduct.getText());
                    int TotalStock = found.getStock() + stock3;
                    found.setStock(TotalStock);
                    JOptionPane.showMessageDialog(this, "Stock actualizado", "Info", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Producto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                }

                break;
            case 9:
                // remove product
                //System.out.println("en el 9");

                String nameDProduct = NProduct.getText();
                Product productoABorrar = shop.findProduct(nameDProduct);

                if (shop.findProduct(nameDProduct) != null) {
                    Shop.inventory.remove(productoABorrar);
                    JOptionPane.showMessageDialog(this, "Producto eliminado", "Info", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Producto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                }

                break;
            default:
                throw new AssertionError();
        }
    }//GEN-LAST:event_OKActionPerformed

