    private void openCashView(int par) {
        CashView dialog = new CashView(this, rootPaneCheckingEnabled,shop);
        dialog.setLocationRelativeTo(this);
        dialog.setModal(true);
        dialog.setVisible(true);

    }

    private void openProductView(int par) {
        ProductView dialog = new ProductView(this, rootPaneCheckingEnabled, par, shop);
        dialog.setLocationRelativeTo(this);
        dialog.setModal(true);
        dialog.setVisible(true);

    }

    public void keyPressed(KeyEvent e) {
        // TODO Auto-generated method stub
        System.out.println("hola");
        if (e.getKeyChar() == KeyEvent.VK_1) {
            this.openCashView(1);
        }
        if (e.getKeyChar() == KeyEvent.VK_2) {
            this.openProductView(2);
        }
        if (e.getKeyChar() == KeyEvent.VK_3) {
            this.openProductView(3);
        }
        if (e.getKeyChar() == KeyEvent.VK_9) {
            this.openProductView(9);
        }
    }

    private void setupKeyBindings() {
        javax.swing.InputMap im = getRootPane().getInputMap(
                javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW
        );
        javax.swing.ActionMap am = getRootPane().getActionMap();

        im.put(javax.swing.KeyStroke.getKeyStroke('1'), "openCash");
        im.put(javax.swing.KeyStroke.getKeyStroke('2'), "openProduct2");
        im.put(javax.swing.KeyStroke.getKeyStroke('3'), "openProduct3");
        im.put(javax.swing.KeyStroke.getKeyStroke('9'), "openProduct9");

        am.put("openCash", new javax.swing.AbstractAction() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                openCashView(1);
            }
        });
        am.put("openProduct2", new javax.swing.AbstractAction() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                openProductView(2);
            }
        });
        am.put("openProduct3", new javax.swing.AbstractAction() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                openProductView(3);
            }
        });
        am.put("openProduct9", new javax.swing.AbstractAction() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                openProductView(9);
            }
        });
    }

